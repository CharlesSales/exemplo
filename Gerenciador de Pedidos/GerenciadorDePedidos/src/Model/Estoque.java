package Model;

public class Estoque {
	private int id;
	private String nome;
	private int quantidade;
	private int inidade;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getQuantidade() {
		return quantidade;
	}
	public void setQuantidade(int quantidade) {
		this.quantidade = quantidade;
	}
	public int getInidade() {
		return inidade;
	}
	public void setInidade(int inidade) {
		this.inidade = inidade;
	}
	
	
}
